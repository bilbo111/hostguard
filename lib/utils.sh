###############################################################################
# Logging
###############################################################################

info() {
    echo -e "${BLUE}[INFO]${NC} $*"
}

success() {
    echo -e "${GREEN}[ OK ]${NC} $*"
}

warn() {
    echo -e "${YELLOW}[WARN]${NC} $*"
}

error() {
    echo -e "${RED}[FAIL]${NC} $*"
}

###############################################################################
# Fatal
###############################################################################

die() {
    error "$*"
    exit 1
}

###############################################################################
# Ask Yes / No
###############################################################################

ask_yes_no() {

    local question="$1"
    local default="${2:-Y}"

    while true
    do

        if [[ "$default" == "Y" ]]; then
            read -rp "$question [Y/n]: " answer
            answer=${answer:-Y}
        else
            read -rp "$question [y/N]: " answer
            answer=${answer:-N}
        fi

        case "$answer" in

            y|Y|yes|YES)
                return 0
                ;;

            n|N|no|NO)
                return 1
                ;;

        esac

    done

}

###############################################################################
# Pause
###############################################################################

pause() {
    echo
    read -rp "Press ENTER to continue..."
}

###############################################################################
# Command exists
###############################################################################

command_exists() {
    command -v "$1" >/dev/null 2>&1
}

###############################################################################
# Internet
###############################################################################

check_internet() {

    if ping -c1 -W3 1.1.1.1 >/dev/null 2>&1; then
        return 0
    fi

    if ping -c1 -W3 8.8.8.8 >/dev/null 2>&1; then
        return 0
    fi

    return 1

}

###############################################################################
# Download helper
###############################################################################

download_file() {

    local url="$1"
    local output="$2"

    if command_exists curl; then

        curl \
            --fail \
            --location \
            --silent \
            --show-error \
            "$url" \
            -o "$output"

        return $?
    fi

    if command_exists wget; then

        wget \
            -q \
            -O "$output" \
            "$url"

        return $?
    fi

    die "Neither curl nor wget found."

}

###############################################################################
# Require command
###############################################################################

require_command() {

    local cmd="$1"

    if ! command_exists "$cmd"; then
        die "Required command '$cmd' not found."
    fi

}

###############################################################################
# Spinner
###############################################################################

run_step() {

    local text="$1"

    shift

    info "$text"

    "$@"

    success "Done."

}

###############################################################################
# Separator
###############################################################################

separator() {
    printf '%*s\n' "${COLUMNS:-80}" '' | tr ' ' '-'
}

###############################################################################
# Detect virtualization
###############################################################################

detect_virtualization() {

    if command_exists systemd-detect-virt; then
        systemd-detect-virt
        return
    fi

    echo "unknown"

}

###############################################################################
# Timestamp
###############################################################################

timestamp() {
    date +"%Y-%m-%d %H:%M:%S"
}

###############################################################################
# Logger
###############################################################################

log() {

    mkdir -p /var/log/hostguard

    echo "[$(timestamp)] $*" >> /var/log/hostguard/install.log

}
